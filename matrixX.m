function [X]=matrixX(my1,my2)
    % ================================================================ 
    %  Matrix X
    % ---------------------------------------------------------------- 
    %  Input 
    %  my1  : Matrix y1    
    %  my2  : Matrix y2
    % ---------------------------------------------------------------- 
    %  Output   
    %  X    : Matrix X baru  
    % ================================================================ 
    lb_m=length(my1(:,1));
    lc_m=length(my2(1,:));
    M00=zeros(lb_m,lc_m);
    X=[my1 M00;M00 my2];
return